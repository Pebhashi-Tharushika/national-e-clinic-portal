            <h2 id="about-us-title">About Us</h2>
            <p>
                Sri Lanka’s healthcare sector has long grappled with challenges such as overcrowded hospitals, limited
                accessibility to medical services, and the need for more streamlined healthcare delivery systems.
                Recognizing these pressing issues, the <strong>National E-Clinic Portal</strong> was conceptualized and
                launched under the guidance of the Ministry of Health. This transformative initiative was spearheaded by
                <strong>Dr. Pushpa Thilakaratne</strong>, the former Minister of Health at that time, alongside a
                dedicated
                committee of leading specialists from the Colombo National Hospital, including <strong>Dr. Oditha
                    Gamlath,
                    Prof. Yapa Bandara De Silva, Prof. Jaliya Gunaratne, and Dr. Sadeshi Jayakodi.</strong> This
                platform
                represents a milestone in advancing the healthcare system of Sri Lanka.
            </p>
            <p>
                The portal aims to revolutionize healthcare in Sri Lanka by leveraging technology to enhance
                accessibility,
                efficiency, and quality of care. Initially introduced in 2032 to serve nine provincial general
                hospitals,
                the service quickly gained traction, expanding to cover general, national, district, base, and
                divisional
                hospitals across the country. Today, more than <strong>400 government hospitals</strong> utilize this
                platform, with plans underway to extend its services to board-managed and specialized hospitals.
            </p>
            <p>
                Operating under the stewardship of the Ministry of Health, the E-Clinic Portal embodies a commitment to
                providing <strong>24×7 access to healthcare</strong>, empowering patients through features like
                <strong>online appointment booking, real-time doctor availability management, and secure patient records
                    management.</strong> By reducing hospital congestion and ensuring efficient service delivery, the
                platform addresses the healthcare needs of Sri Lankans while minimizing financial burdens.
            </p>
            <p>
                The National E-Clinic Portal represents a significant leap towards achieving universal healthcare access
                and
                improving the overall health standards of the nation. It is more than just a platform; it is a vision to
                make quality healthcare accessible to all Sri Lankans, transforming the healthcare experience and
                contributing to the socio-economic development of the country.
            </p>
        </div>

